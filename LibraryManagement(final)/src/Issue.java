    public void IssueUpdate(){
        String sql ="Insert into Issue(Book_ID, Book_Name, Genre, Edition, Price, Student_ID, Name, Surname, Major, Year, DateOfIssue) values (?,?,?,?,?,?,?,?,?,?,?)";
        try {
            pst=conn.prepareCall(sql);
            pst.setString(1, BookIDField.getText());
            pst.setString(2, BookNameField.getText());
            pst.setString(3, GenreField.getText());
            pst.setString(4, EditionField.getText());
            pst.setString(5, PriceField.getText());
            pst.setString(6, StudentIDField.getText());
            pst.setString(7, NameField.getText());
            pst.setString(8, SurnameField.getText());
            pst.setString(9, MajorField.getText());
            pst.setString(10, YearField.getText());
            pst.setString(11,((JTextField)FindDateIssue.getDateEditor().getUiComponent()).getText());
            pst.execute();
            JOptionPane.showMessageDialog(null, "Book Issued");
        }catch (HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
