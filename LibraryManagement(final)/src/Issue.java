    private void SearchBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchBookButtonActionPerformed
        // TODO add your handling code here:
        String sql = "select * from NBook where Book_ID=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1,BookIDField.getText());
            rs=pst.executeQuery();
            if(rs.next()){
                String add1 = rs.getString("Book_Name");
                BookNameField.setText(add1);
                String add2 = rs.getString("Edition");
                EditionField.setText(add2);
                String add3 = rs.getString("Genre");
                GenreField.setText(add3);
                String add4 = rs.getString("Price");
                PriceField.setText(add4);
                rs.close();
                pst.close();
            }    
                else{
                        JOptionPane.showMessageDialog(null, "Book ID is not found!");
                        }
            
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null, e);
            } finally{
            try {
                rs.close();
                pst.close();
            }catch(SQLException e){
                
            }
        }
    }//GEN-LAST:event_SearchBookButtonActionPerformed

